package com.travel.po;

public class Province {
	private Integer province_id;
	private String province_name;
	private String province_img;
	private String province_introduce;
	private Integer country_id;
	@Override
	public String toString() {
		return "Province [province_id=" + province_id + ", province_name="
				+ province_name + ", province_img=" + province_img
				+ ", province_introduce=" + province_introduce
				+ ", country_id=" + country_id + "]";
	}
	public Integer getProvince_id() {
		return province_id;
	}
	public void setProvince_id(Integer province_id) {
		this.province_id = province_id;
	}
	public String getProvince_name() {
		return province_name;
	}
	public void setProvince_name(String province_name) {
		this.province_name = province_name;
	}
	public String getProvince_img() {
		return province_img;
	}
	public void setProvince_img(String province_img) {
		this.province_img = province_img;
	}
	public String getProvince_introduce() {
		return province_introduce;
	}
	public void setProvince_introduce(String province_introduce) {
		this.province_introduce = province_introduce;
	}
	public Integer getCountry_id() {
		return country_id;
	}
	public void setCountry_id(Integer country_id) {
		this.country_id = country_id;
	}
	
	
}
